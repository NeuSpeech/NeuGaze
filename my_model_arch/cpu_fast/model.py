        x = self.layer3(x)
        x = self.layer4(x)
        x = self.avgpool(x)
        x = x.view(x.size(0), -1)

        # gaze
        pre_yaw_gaze = self.fc_yaw_gaze(x)
        pre_pitch_gaze = self.fc_pitch_gaze(x)
        # pre_yaw_gaze = F.relu(pre_yaw_gaze)
        # pre_pitch_gaze = F.relu(pre_pitch_gaze)

        pre_x_gaze = self.fc_x_gaze(pre_yaw_gaze)
        pre_y_gaze = self.fc_y_gaze(pre_pitch_gaze)
        pre_x_gaze = F.sigmoid(pre_x_gaze)
        pre_y_gaze = F.sigmoid(pre_y_gaze)
        return torch.cat([pre_x_gaze,pre_y_gaze],dim=1)


